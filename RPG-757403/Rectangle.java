

public class Rectangle {
    int x;
    int y;
    int width;
    int height;

    public Rectangle(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int getX(int x) {
        return x;
    }
    public int getY(int y) {
        return y;
    }
    public int getWidth(int width) {
        return width;
    }
    public int getHeight(int height) {
        return height;
    }
}
